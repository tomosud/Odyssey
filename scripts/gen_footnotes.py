def parse_en(md):
    body = re.sub(r"^---[\s\S]*?---\s*", "", md)
    m = {}
    for blk in re.split(r"\n\s*\n", body):
        mm = re.match(r"^\s*\[(\d+)\]\s*([\s\S]*)$", blk)
        if not mm:
            # 29[] のような表記ゆれも拾う
            mm = re.match(r"^\s*(\d+)\[\]\s*([\s\S]*)$", blk)
            if not mm:
                continue
        num = mm.group(1)
        text = re.sub(r"\s+", " ", mm.group(2)).strip()
        text = re.sub(r"^\[\s*", "", text)
        text = re.sub(r"\s*\]$", "", text)
        m[num] = text
    return m

